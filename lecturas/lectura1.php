						<p> Adem&aacute;s de los principios operativos mencionados anteriormente, se sugiere un conjunto de principios directrices para la ingeniería de requerimientos:</p></br>

					<blockquote></br>
						<ol> 1. Entender el problema antes de empezar a crear el modelo de análisis.</ol>
						<ol> 2. Desarrollar prototipos que permitan al usuario entender cómo será la interacción hombre-máquina.</ol>
						<ol> 3. Registrar el orden y la razón de cada requerimiento,</ol>
						<ol> 4. Usar múltiples planteamientos de requerimientos.</ol>
						<ol> 5. Priorizar los requerimientos.</ol>
						<ol> 6. Trabajar para eliminar la ambigüedad.</ol>
